/*

This program explains about various Arithmetic operators in C

Usage	: exp1 + exp2
	An expression  can be any integer or floating-point expression

*/

#include <stdio.h>

int main()
{

const int a=10, b=2;

	printf("\n a = %d <==> b = %d \n",a,b);
	
	// + used for addition
	printf("\n a + b = %d \n",a+b);

	// - used for subtration
	printf("\n a - b = %d \n",a-b);

	// / used for division
	printf("\n a / b = %d \n",a/b);
	
	// % gives reminder of the given expression
	printf("\n a %% b = %d \n",a%b);

}


