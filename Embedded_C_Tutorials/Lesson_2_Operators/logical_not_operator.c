/*

This program explains that ! (Logical Negation or NOT) operator

The logical negation operator (!) takes onlt one operand.
If the operand is true, the result is false; if the operand is false, the result is true.

Usage	: !exp1
	  exp1 and exp2 can be any expression
*/ 

#include <stdio.h>

int main()
{
	int a = 1;

	printf("\n a = %d , !a = %d \n",a,!a);

	a = 0;
	printf("\n a = %d , !a = %d \n",a,!a);
}
